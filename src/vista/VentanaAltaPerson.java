    private void jMIAltaPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMIAltaPersonActionPerformed
        boolean dok = true;
        try{
            if(jFDni.getText().isEmpty()||jTNombreP.getText().isEmpty()||jTApellido1.getText().isEmpty()||jTApellido2.getText().isEmpty()
                    ||jFTelefono.getText().isEmpty()){throw new DatoVacio();}
            
            if(jCBEvento.getSelectedIndex()<=0||jCBNifEmp.getSelectedIndex()<=0){throw new DatoVacio();}
            
            if(jCBNifEmp.getSelectedIndex()==jCBNifEmp.getItemCount()-1){
                dok = false;
                ejercicioDBO4.Sig_VAltaEmp();
            }
            if(dok==true){
                //AsistenteAlta
                
                jCBEvento.removeAllItems();
                jCBEvento.addItem("-- Seleccione un Evento --");
                ArrayList<String> ListaCB = new ArrayList();
                //RllenarCBEvent
                
                for(int x = 0; ListaCB.size()>x; x++){
                    String Nombre = ListaCB.get(x);
                    jCBEvento.addItem(Nombre);
                }
                
                JOptionPane.showMessageDialog(this,"Asistente dado de alta.");
                jCBEvento.setSelectedIndex(0);
                jFDni.setText(null);
                jTNombreP.setText(null);
                jTApellido1.setText(null);
                jTApellido2.setText(null);
                jFTelefono.setText(null);
                jCBNifEmp.setSelectedIndex(0);
                
            }
            
        }catch(DatoVacio Ex){JOptionPane.showMessageDialog(this,"Error:\n Todo los datos son obligatorios"); dok=false;
        }catch(Exception Ex){JOptionPane.showMessageDialog(this,"Error:\n"+Ex.getMessage()); dok=false;}
    }//GEN-LAST:event_jMIAltaPersonActionPerformed

